def tune_xgb(X_tr, y_tr, X_vl, y_vl, n_trials=OPTUNA_TRIALS):
    def obj(trial):
        p = dict(
            n_estimators     = trial.suggest_int("n_estimators", 100, 500),
            max_depth        = trial.suggest_int("max_depth", 3, 7),
            learning_rate    = trial.suggest_float("learning_rate", 0.01, 0.15, log=True),
            subsample        = trial.suggest_float("subsample", 0.6, 1.0),
            colsample_bytree = trial.suggest_float("colsample_bytree", 0.5, 1.0),
            min_child_weight = trial.suggest_int("min_child_weight", 1, 10),
            reg_alpha        = trial.suggest_float("reg_alpha", 1e-4, 1.0, log=True),
            reg_lambda       = trial.suggest_float("reg_lambda", 1e-4, 5.0, log=True),
            eval_metric="logloss", random_state=RANDOM_STATE, verbosity=0,
        )
        m = XGBClassifier(**p)
        m.fit(X_tr, y_tr, eval_set=[(X_vl, y_vl)], verbose=False)
        prob = m.predict_proba(X_vl)[:,1]
        return roc_auc_score(y_vl, prob) if len(np.unique(y_vl)) > 1 else 0.5
    study = optuna.create_study(direction="maximize",
                                sampler=optuna.samplers.TPESampler(seed=RANDOM_STATE))
    study.optimize(obj, n_trials=n_trials, show_progress_bar=False)
    return study.best_params

def tune_lgb(X_tr, y_tr, X_vl, y_vl, n_trials=OPTUNA_TRIALS):
    def obj(trial):
        p = dict(
            n_estimators     = trial.suggest_int("n_estimators", 100, 500),
            max_depth        = trial.suggest_int("max_depth", 3, 8),
            learning_rate    = trial.suggest_float("learning_rate", 0.01, 0.15, log=True),
            num_leaves       = trial.suggest_int("num_leaves", 20, 100),
            subsample        = trial.suggest_float("subsample", 0.6, 1.0),
            colsample_bytree = trial.suggest_float("colsample_bytree", 0.5, 1.0),
            min_child_samples= trial.suggest_int("min_child_samples", 5, 50),
            reg_alpha        = trial.suggest_float("reg_alpha", 1e-4, 1.0, log=True),
            random_state=RANDOM_STATE, verbosity=-1,
        )
        m = lgb.LGBMClassifier(**p)
        m.fit(X_tr, y_tr, eval_set=[(X_vl, y_vl)],
              callbacks=[lgb.early_stopping(20, verbose=False), lgb.log_evaluation(-1)])
        prob = m.predict_proba(X_vl)[:,1]
        return roc_auc_score(y_vl, prob) if len(np.unique(y_vl)) > 1 else 0.5
    study = optuna.create_study(direction="maximize",
                                sampler=optuna.samplers.TPESampler(seed=RANDOM_STATE))
    study.optimize(obj, n_trials=n_trials, show_progress_bar=False)
    return study.best_params


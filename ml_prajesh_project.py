def drawdown_series(s):
    eq_ = (1+s).cumprod()
    return (eq_ - eq_.cummax()) / eq_.cummax()

def risk_report(s):
    n      = len(s)
    cagr   = (1+s).prod()**(12/n)-1
    ann_v  = s.std()*np.sqrt(12)
    sharpe = (s.mean()/s.std())*np.sqrt(12) if s.std()>0 else np.nan
    eq_    = (1+s).cumprod(); dd_ = (eq_-eq_.cummax())/eq_.cummax()
    mdd    = float(dd_.min())
    calmar = cagr/abs(mdd) if mdd!=0 else np.nan
    hit    = float((s>0).mean())
    neg_s  = s[s<0]
    sor_d  = neg_s.std()*np.sqrt(12) if len(neg_s)>1 else np.nan
    sort_  = (s.mean()*12)/sor_d if (sor_d and sor_d>0) else np.nan
    aln    = pd.concat([s, r_nifty], axis=1).dropna(); aln.columns = ["s","n"]
    if len(aln)>5 and aln["n"].std()>0:
        cv   = np.cov(aln["s"].values, aln["n"].values)
        beta = cv[0,1]/cv[1,1]
        alph = (aln["s"].mean()-beta*aln["n"].mean())*12
    else:
        beta, alph = np.nan, np.nan
    final = INITIAL_CAPITAL*(1+s).prod()
    return {
        "CAGR (%)":      round(cagr*100, 2),
        "Ann Vol (%)":   round(ann_v*100, 2),
        "Sharpe":        round(sharpe, 3)  if not np.isnan(sharpe) else "N/A",
        "Sortino":       round(sort_, 3)   if not np.isnan(sort_)  else "N/A",
        "Max DD (%)":    round(mdd*100, 2),
        "Calmar":        round(calmar, 3)  if not np.isnan(calmar) else "N/A",
        "Hit Rate (%)":  round(hit*100, 2),
        "Beta":          round(beta, 3)    if not np.isnan(beta)   else "N/A",
        "Alpha ann (%)": round(alph*100,2) if not np.isnan(alph)   else "N/A",
        "Final Value ₹": f"₹{final:,.0f}",
    }


def rolling_beta(group, window=36):
    sr = group["ret_1m"].values; mr = group["nifty_ret"].values
    betas = []
    for i in range(len(group)):
        if i < window-1:
            betas.append(np.nan); continue
        s_ = sr[i-window+1:i+1]; m_ = mr[i-window+1:i+1]
        mask = ~(np.isnan(s_) | np.isnan(m_))
        if mask.sum() < 10:
            betas.append(np.nan)
        else:
            cv = np.cov(s_[mask], m_[mask])
            betas.append(cv[0,1]/cv[1,1] if cv[1,1]!=0 else np.nan)
    return betas


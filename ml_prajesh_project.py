def build_portfolio(today_df, regime_id):
    mname, model = best_model_per_regime.get(regime_id, ("global", global_cal))
    today = today_df.dropna(subset=FEATURE_COLS).copy()
    if len(today) < TOP_K: return None, None, None
    X = scaler_global.transform(today[FEATURE_COLS].values)
    today["score"] = model.predict_proba(X)[:,1]
    top = today.nlargest(TOP_K, "score").copy()
    vol = top["vol_12m"].replace(0, np.nan).fillna(0.04).clip(lower=1e-6)
    w   = 1.0/vol; w /= w.sum()
    w   = w.clip(upper=MAX_WEIGHT); w /= w.sum()
    top["sec_"] = top["stock"].map(SECTOR_MAP).fillna("Other")
    for _, grp in top.groupby("sec_"):
        sw = w.loc[grp.index].sum()
        if sw > SECTOR_CAP: w.loc[grp.index] *= SECTOR_CAP/sw
    w /= w.sum()
    return top["stock"].values, w.values, mname


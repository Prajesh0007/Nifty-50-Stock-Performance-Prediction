# If 4 components used, merge the two most similar
if best_n == N_REGIMES + 1:
    means = best_hmm.means_
    min_dist, merge_pair = np.inf, (0,1)
    for a in range(best_n):
        for b in range(a+1, best_n):
            d = np.linalg.norm(means[a] - means[b])
            if d < min_dist:
                min_dist, merge_pair = d, (a, b)
    label_map = {i: i for i in range(best_n)}
    label_map[merge_pair[1]] = merge_pair[0]
    raw_labels = np.array([label_map[l] for l in raw_labels])
    unique_l = sorted(set(raw_labels))
    reindex  = {old: new for new, old in enumerate(unique_l)}
    raw_labels = np.array([reindex[l] for l in raw_labels])


# RSI
def compute_rsi(series, period=14):
    delta = series.diff()
    gain  = delta.clip(lower=0).rolling(period, min_periods=period//2).mean()
    loss  = (-delta.clip(upper=0)).rolling(period, min_periods=period//2).mean()
    rs    = gain / loss.replace(0, np.nan)
    return 100 - (100 / (1 + rs))

stocks["rsi_14"] = stocks.groupby("stock")["price"].transform(compute_rsi)

# Bollinger Band %
def bb_pct(series, w=20, k=2):
    mid   = series.rolling(w, min_periods=w//2).mean()
    std   = series.rolling(w, min_periods=w//2).std()
    upper = mid + k*std; lower = mid - k*std
    return (series - lower) / (upper - lower + 1e-9)

stocks["bb_pct"] = stocks.groupby("stock")["price"].transform(bb_pct)

